DELETE FROM mpas;
DELETE FROM genres;
DELETE FROM friendship;
DELETE FROM likes;
DELETE FROM films_genres;
DELETE FROM directors;
DELETE FROM films_directors;
DELETE FROM reviews;
DELETE FROM reviews_likes;
DELETE FROM events;